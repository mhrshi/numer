package nm.droid.com.numericalmethods.singleequation;

class SingleEquationRow {

    String mIteration;
    String mLEP;
    String mREP;
    String mX;
    String mFX;

    SingleEquationRow(String iteration, String LEP, String REP, String X, String FX) {
        mIteration = iteration;
        mLEP = LEP;
        mREP = REP;
        mX = X;
        mFX = FX;
    }
}
